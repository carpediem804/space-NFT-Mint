export const usePlanetContract = (web3: Web3 | null) => {
  if (!web3) {
    return {
      contractAddress: CONTRACT_ADDRESS,
      mintPlanet: (options: SendOptions | undefined) => {
        throw new Error("Wallet not installed");
      },

      tokenURI: (tokenID: number) => {
        throw new Error("Wallet not installed");
      },
      ownerOf: (tokenID: number) => {
        throw new Error("Wallet not installed");
      },
      totalSupply: () => {
        throw new Error("Wallet not installed");
      },
    };
  }
  const contract = new web3.eth.Contract(ABI as AbiItem[], CONTRACT_ADDRESS);

  return {
    contractAddress: CONTRACT_ADDRESS,
    mintPlanet: (options: SendOptions | undefined) =>
      contract.methods.mintPlanet().send(options),
    tokenURI: (tokenID: number) => contract.methods.tokenURI(tokenID).call(),
    ownerOf: (tokenID: number) => contract.methods.ownerOf(tokenID).call(),
    totalSupply: () => contract.methods.totalSupply().call(),
  };
};
